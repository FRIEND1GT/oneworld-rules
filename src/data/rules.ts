export const getRules = (): Section[] => {
  const sections: Section[] = [];
  let currentSection: Section | null = null;
  
  const lines = rawText.split('\n').map(l => l.trim()).filter(l => l);
  
  for (const line of lines) {
    if (line.startsWith('Раздел')) {
      if (currentSection) {
        sections.push(currentSection);
      }
      currentSection = { title: line, rules: [] };
    } else if (currentSection && /^\d+\.\d+/.test(line)) {
      const match = line.match(/^(\d+\.\d+)\s+(.*)$/);
      if (match) {
        let text = match[2];
        let punishment;
        const punishmentMatch = text.match(/\[(.*?)\]$/);
        if (punishmentMatch) {
          punishment = punishmentMatch[1];
          text = text.replace(/\[.*?\]$/, '').trim();
        }
        currentSection.rules.push({ id: match[1], text, punishment });
      }
    } else if (/^99\.99/.test(line)) {
      const ruleText = line.replace(/^99\.99\s*/, '');
      if (currentSection) {
        currentSection.rules.push({ id: "99.99", text: ruleText });
      } else {
        sections.push({
          title: "Секретный раздел",
          rules: [{ id: "99.99", text: ruleText }]
        });
      }
    }
  }
  if (currentSection) {
    sections.push(currentSection);
  }
  return sections;
};

